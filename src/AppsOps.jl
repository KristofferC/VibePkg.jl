# The apps a package tree declares, with the entry-point module names
# resolved (`Pkg` or `Pkg.Submodule`).
function declared_apps(pkg_root::String, pkg_name::String)
    project = read_project(projectfile_path(pkg_root))
    apps = Dict{String, AppInfo}()
    for (app_name, info) in project.apps
        submodule = info.submodule === nothing ? pkg_name : "$pkg_name.$(info.submodule)"
        apps[app_name] = AppInfo(
            app_name, joinpath(Sys.BINDIR, "julia"), submodule,
            info.julia_flags, Dict{String, Any}(),
        )
    end
    isempty(apps) && pkgerror("package `$pkg_name` at `$pkg_root` does not declare any apps (no `[apps]` section)")
    return apps
end

function validate_app_collisions(manifest::Manifest, uuid::UUID, apps::Dict{String, AppInfo})
    for (other_uuid, entry) in manifest
        other_uuid == uuid && continue
        collisions = sort!(collect(intersect(keys(apps), keys(entry.apps))))
        isempty(collisions) && continue
        noun = length(collisions) == 1 ? "app name" : "app names"
        pkgerror(
            "$noun $(join(repr.(collisions), ", ")) already installed by package `$(entry.name)`"
        )
    end
    return nothing
end

function remove_or_restore_shim!(d::DepotStack, manifest::Manifest, app_name::String)
    for (_, entry) in manifest
        app = get(entry.apps, app_name, nothing)
        app === nothing || return rewrite_shim(d, entry, app)
    end
    Base.rm(shim_path(d, app_name); force = true)
    return nothing
end

function record_app_package!(d::DepotStack, entry::ManifestEntry)
    manifest = read_app_manifest(d)
    validate_app_collisions(manifest, entry.uuid, entry.apps)
    previous = get(manifest, entry.uuid, nothing)
    deps = Dict{UUID, ManifestEntry}(manifest.deps)
    deps[entry.uuid] = entry
    updated = with_manifest(manifest; deps, julia_version = VERSION)
    write_app_manifest(d, updated)
    if previous !== nothing
        for app_name in setdiff(keys(previous.apps), keys(entry.apps))
            remove_or_restore_shim!(d, updated, app_name)
        end
    end
    return nothing
end

function replace_app_environment!(staging::String, destination::String)
    backup = nothing
    if ispath(destination)
        backup = tempname(dirname(destination))
        mv(destination, backup)
    end
    try
        mv(staging, destination)
    catch
        if backup !== nothing && ispath(backup)
            Base.rm(destination; force = true, recursive = true)
            mv(backup, destination)
        end
        rethrow()
    end
    backup === nothing || Base.rm(backup; force = true, recursive = true)
    return nothing
end

function print_path_hint(d::DepotStack, io::IO)
    bindir = bin_dir(depots1(d))
    if !occursin(bindir, get(ENV, "PATH", ""))
        printpkgstyle(io, :Info, "add $(pathrepr(bindir)) to your PATH to run the installed apps", color = Base.info_color())
    end
    return
end

