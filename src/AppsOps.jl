# Apps.
#
# A package declares apps in its Project.toml:
#
#     [apps]
#     myapp = { submodule = "CLI", julia_flags = ["-q"] }
#
# and provides an entry point via `@main` (run with `julia -m Pkg[.Sub]`).
# Installing creates a per-package environment under
# `<depot>/environments/apps/<PkgName>/`, records everything in the global
# `<depot>/environments/apps/AppManifest.toml` (an ordinary manifest whose
# entries carry the `apps` tables), and writes shims into `<depot>/bin/`.
#
# Shims self-locate their depot (so a moved depot keeps working), pin the
# installing julia by absolute path, and honor `JULIA_APPS_JULIA_CMD` as an
# override. All mutating operations run under a pidlock.
#
# Shim-format bumps reach already-installed apps via `migrate_shims!`, run at
# the start of every mutating operation: it version-checks each installed shim
# and rewrites the stale ones straight from the recorded manifest (no
# reinstall).

module AppsOps

