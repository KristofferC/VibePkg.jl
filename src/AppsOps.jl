"""
    app_develop(config, registries, path; io)

Track the package at `path` as an app source: its own environment is
instantiated in place and the shims point straight at it.
"""
function app_develop(config::Config, registries::Vector{RegistryInstance}, path::String; io::IO = stderr_f())
    d = config.depots
    return app_lock(d) do
        migrate_shims!(d; io)
        dev_dir = abspath(path)
        project_file = projectfile_path(dev_dir; strict = true)
        project_file === nothing && pkgerror("could not find project file in package at `$path`")
        project = read_project(project_file)
        (project.name === nothing || project.uuid === nothing) &&
            pkgerror("expected a `name` and `uuid` entry in project file at `$project_file`")
        apps = declared_apps(dev_dir, project.name)
        validate_app_collisions(read_app_manifest(d), project.uuid, apps)

        # make the dev'd project itself loadable
        env = Environments.load_environment_from(project_file; depots = d)
        planned = plan_resolve(env, registries, config)
        Execution.apply!(env, planned, registries, config; io)

        entry = ManifestEntry(
            project.name, project.uuid, PathTracked(dev_dir, project.version), false,
            Dict{String, UUID}(), Dict{String, UUID}(),
            Dict{String, Union{String, Vector{String}}}(), apps,
            nothing, nothing, Dict{String, Any}(),
        )
        record_app_package!(d, entry)
        for app in values(apps)
            write_shim(d, app, dev_dir; relative_load_path = false)
            printpkgstyle(io, :Installed, "app `$(app.name)`")
        end
        print_path_hint(d, io)
        nothing
    end
end

"""
    app_add(config, registries, request; io)

Install a registered package as an app: a dedicated environment under
`environments/apps/<Name>/` is resolved and instantiated, and shims are
written for every app the package declares.
"""
function app_add(config::Config, registries::Vector{RegistryInstance}, request::PackageRequest; io::IO = stderr_f())
    d = config.depots
    return app_lock(d) do
        migrate_shims!(d; io)
        # resolve the request up-front (against an empty environment, so
        # purely from registries/stdlibs): the environment directory and the
        # shim load path must use the same name, also for uuid-only requests
        name, uuid = Planning.resolve_request(
            Environments.load_environment_from(joinpath(apps_dir(d), "Project.toml"); depots = d),
            registries, request,
        )
        # Build the per-package environment off to the side. A failed resolve,
        # download, or metadata check must leave the currently installed app
        # runnable.
        env_dir = joinpath(apps_dir(d), name)
        entry, apps = mktempdir(apps_dir(d); prefix = ".install-") do staging
            env = Environments.load_environment_from(joinpath(staging, "Project.toml"); depots = d)
            planned = plan_add(env, registries, config, [request])
            result = Execution.apply!(env, planned, registries, config; io)
            entry = result.env.manifest[uuid]
            source = Execution.entry_source_path(result.env.manifest_file, entry, d)
            apps = declared_apps(source, name)
            validate_app_collisions(read_app_manifest(d), uuid, apps)
            replace_app_environment!(staging, env_dir)
            (entry, apps)
        end
        record_app_package!(d, with_entry(entry; apps))
        for app in values(apps)
            # depot-relative load path keeps relocated depots working
            write_shim(d, app, "environments/apps/$name"; relative_load_path = true)
            printpkgstyle(io, :Installed, "app `$(app.name)`")
        end
        print_path_hint(d, io)
        nothing
    end
end

"""
    app_rm(depots, name; io)

Remove by package name (all its apps, its environment, its entry) or by app
name (that app from every package, dropping packages left appless).
"""
function app_rm(d::DepotStack, name::String; io::IO = stderr_f())
    return app_lock(d) do
        manifest = read_app_manifest(d)
        deps = Dict{UUID, ManifestEntry}(manifest.deps)
        removed_apps = String[]
        removed_pkgs = UUID[]
        for (uuid, entry) in deps
            if entry.name == name
                append!(removed_apps, keys(entry.apps))
                push!(removed_pkgs, uuid)
            elseif haskey(entry.apps, name)
                push!(removed_apps, name)
                apps = Dict{String, AppInfo}(entry.apps)
                delete!(apps, name)
                if isempty(apps)
                    push!(removed_pkgs, uuid)
                else
                    deps[uuid] = with_entry(entry; apps)
                end
            end
        end
        isempty(removed_apps) && isempty(removed_pkgs) &&
            pkgerror("no app or app-providing package named `$name` is installed")
        for uuid in removed_pkgs
            entry = deps[uuid]
            entry_path(entry) === nothing && Base.rm(joinpath(apps_dir(d), entry.name); force = true, recursive = true)
            delete!(deps, uuid)
        end
        write_app_manifest(d, with_manifest(manifest; deps))
        for app in unique!(removed_apps)
            remove_or_restore_shim!(d, with_manifest(manifest; deps), app)
            printpkgstyle(io, :Removed, "app `$app`")
        end
        nothing
    end
end

"""
    app_update(config, registries, [name]; io)

Update installed apps: registry-tracked app packages are reinstalled at
their latest resolvable version, path-tracked ones re-resolve in place;
shims are rewritten either way. `name` (a package or app name) restricts
the update; with no name every installed app updates.
"""
function app_update(
        config::Config, registries::Vector{RegistryInstance},
        name::Union{Nothing, String} = nothing; io::IO = stderr_f(),
    )
    d = config.depots
    manifest = read_app_manifest(d)
    targets = ManifestEntry[]
    for (uuid, entry) in manifest
        if name === nothing || entry.name == name || haskey(entry.apps, name)
            push!(targets, entry)
        end
    end
    if isempty(targets)
        name === nothing ||
            pkgerror("no app or app-providing package named `$name` is installed")
        printpkgstyle(io, :Info, "no apps installed", color = Base.info_color())
        return nothing
    end
    # app_add/app_develop take the app lock themselves, so no lock here
    for entry in targets
        path = entry_path(entry)
        if path !== nothing
            app_develop(config, registries, path; io)
        else
            app_add(config, registries, PackageRequest(entry.name, entry.uuid, nothing); io)
        end
    end
    return nothing
end

"""
    app_status(depots, [filter_names]; io)

List installed app packages and their apps, optionally restricted to the
given package or app names.
"""
function app_status(d::DepotStack, filter_names::Vector{String} = String[]; io::IO = stderr_f())
    manifest = read_app_manifest(d)
    printpkgstyle(io, :Status, pathrepr(app_manifest_file(d)))
    for (uuid, entry) in manifest
        isempty(filter_names) || entry.name in filter_names ||
            any(in(filter_names), keys(entry.apps)) || continue
        v = EnvFiles.entry_version(entry)
        tracking = entry_path(entry) !== nothing ? " `$(entry_path(entry))`" : ""
        println(io, "  [", string(uuid)[1:8], "] ", entry.name, v === nothing ? "" : " v$v", tracking)
        for (app_name, app) in entry.apps
            println(io, "    ", app_name)
        end
    end
    return
end

