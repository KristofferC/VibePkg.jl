export app_add, app_develop, app_rm, app_update, app_status

const SHIM_VERSION = "1.1"

apps_dir(d::DepotStack) = joinpath(environments_dir(depots1(d)), "apps")
app_manifest_file(d::DepotStack) = joinpath(apps_dir(d), "AppManifest.toml")

read_app_manifest(d::DepotStack) = read_manifest(app_manifest_file(d))

function write_app_manifest(d::DepotStack, manifest::Manifest)
    mkpath(apps_dir(d))
    return write_manifest(manifest, app_manifest_file(d))
end

app_lock(f, d::DepotStack) = mkpidlock(f, joinpath(mkpath(apps_dir(d)), ".pid"); stale_age = 10)

#########
# Shims #
#########

function shim_path(d::DepotStack, app_name::String)
    return joinpath(bin_dir(depots1(d)), Sys.iswindows() ? app_name * ".bat" : app_name)
end

# Shims self-locate the depot when the script still lives in `<depot>/bin`,
# otherwise fall back to the install-time depot; the app environment is
# addressed depot-relative so relocated depots keep working. Argument
# protocol (Pkg parity): args before a `--` go to julia, args after it to
# the app; without `--` everything goes to the app.
function shim_contents(
        app::AppInfo, depot::String, load_path::String;
        relative_load_path::Bool, windows::Bool = Sys.iswindows(),
    )
    julia = something(app.julia_command, joinpath(Sys.BINDIR, "julia"))
    entry = app.submodule === nothing ? app.name : app.submodule
    return windows ? bat_shim(app, julia, entry, depot, load_path; relative_load_path) :
        sh_shim(app, julia, entry, depot, load_path; relative_load_path)
end

function sh_shim(app::AppInfo, julia::String, entry::String, depot::String, load_path::String; relative_load_path::Bool)
    julia_escaped = Base.shell_escape(julia)
    entry_escaped = Base.shell_escape(entry)
    flags_part = isempty(app.julia_flags) ? "" : " " * join(Base.shell_escape.(app.julia_flags), " ")
    lp = relative_load_path ? "\"\$DEPOT/$load_path\"" : Base.shell_escape(load_path)
    return """
    #!/bin/sh
    set -eu
    # generated by VibePkg (shim version $SHIM_VERSION); do not edit
    SCRIPT_DIR=\$(CDPATH= cd -- "\$(dirname -- "\$0")" && pwd -P)
    if [ -f "\$SCRIPT_DIR/../environments/apps/AppManifest.toml" ]; then
        DEPOT=\$(cd -- "\$SCRIPT_DIR/.." >/dev/null 2>&1 && pwd -P)
    else
        DEPOT=$(Base.shell_escape(depot))
    fi
    JULIA=\${JULIA_APPS_JULIA_CMD:-$julia_escaped}
    export JULIA_DEPOT_PATH="\$DEPOT:"
    export JULIA_LOAD_PATH=$lp

    # `--` splits julia args (before) from app args (after); with no `--`
    # every argument goes to the app. Rebuild "\$@" in place as
    # <julia args> -m <module> <app args>, preserving argument boundaries.
    orig_count=\$#
    seen_sep=false
    i=0
    while [ "\$i" -lt "\$orig_count" ]; do
        arg=\$1
        shift
        if [ "\$seen_sep" = false ] && [ "\$arg" = "--" ]; then
            seen_sep=true
            set -- "\$@" -m $entry_escaped
        else
            set -- "\$@" "\$arg"
        fi
        i=\$((i + 1))
    done
    if [ "\$seen_sep" = false ]; then
        set -- -m $entry_escaped "\$@"
    fi

    exec "\$JULIA" --startup-file=no$flags_part "\$@"
    """
end

function bat_shim(app::AppInfo, julia::String, entry::String, depot::String, load_path::String; relative_load_path::Bool)
    julia_escaped = "\"$(Base.shell_escape_wincmd(julia))\""
    entry_escaped = "\"$(Base.shell_escape_wincmd(entry))\""
    flags_part = isempty(app.julia_flags) ? "" : " " * join(Base.shell_escape_wincmd.(app.julia_flags), " ")
    lp = relative_load_path ? "%depot%\\$(join(splitpath(load_path), '\\'))" : normpath(load_path)
    return """
    @echo off
    setlocal EnableExtensions DisableDelayedExpansion

    rem generated by VibePkg (shim version $SHIM_VERSION); do not edit

    rem Locate the depot from this script (<depot>\\bin\\<app>.bat) so a
    rem relocated depot keeps working; fall back to the install-time depot
    for %%I in ("%~dp0..") do set "depot=%%~fI"
    if not exist "%depot%\\environments\\apps\\AppManifest.toml" set "depot=$(normpath(depot))"

    rem (no delayed expansion here so '!' stays literal; trailing ';' keeps
    rem the default system depots)
    set "JULIA_LOAD_PATH=$lp"
    set "JULIA_DEPOT_PATH=%depot%;"

    if defined JULIA_APPS_JULIA_CMD (
        set "julia_cmd=%JULIA_APPS_JULIA_CMD%"
    ) else (
        set "julia_cmd=$julia_escaped"
    )

    setlocal EnableDelayedExpansion

    rem Parse arguments, splitting on the first -- into julia_args / app_args
    set "found_sep="
    set "julia_args="
    set "app_args="

    :__next
    if "%~1"=="" goto __done

    if not defined found_sep if "%~1"=="--" (
        set "found_sep=1"
        shift
        goto __next
    )

    if not defined found_sep (
        if defined julia_args (
            set "julia_args=!julia_args! %1"
        ) else (
            set "julia_args=%1"
        )
        shift
        goto __next
    )

    if defined found_sep (
        if defined app_args (
            set "app_args=!app_args! %1"
        ) else (
            set "app_args=%1"
        )
        shift
        goto __next
    )

    :__done
    rem With no -- every original argument goes to the app
    if defined found_sep (
        "%julia_cmd%" ^
            --startup-file=no$flags_part !julia_args! ^
            -m $entry_escaped ^
            !app_args!
    ) else (
        "%julia_cmd%" ^
            --startup-file=no$flags_part ^
            -m $entry_escaped ^
            %*
    )
    """
end

function write_shim(d::DepotStack, app::AppInfo, load_path::String; relative_load_path::Bool)
    mkpath(bin_dir(depots1(d)))
    path = shim_path(d, app.name)
    content = shim_contents(app, depots1(d), load_path; relative_load_path)
    if Sys.iswindows()
        # batch files need CRLF line endings for reliable label parsing
        content = replace(replace(content, "\r\n" => "\n"), "\n" => "\r\n")
        write(path, content)
    else
        write(path, content)
        chmod(path, 0o755)
    end
    return path
end

# The shim format version embedded in an installed shim, or `nothing` if the
# file is absent or not a VibePkg-generated shim.
function installed_shim_version(path::String)
    isfile(path) || return nothing
    m = match(r"shim version ([0-9.]+)", read(path, String))
    return m === nothing ? nothing : String(m.captures[1]::SubString{String})
end

# Regenerate the shim for one recorded app from its manifest entry, matching
# the load-path form the original install used: registry-tracked apps get the
# depot-relative path, path-tracked (develop) apps their absolute source.
function rewrite_shim(d::DepotStack, entry::ManifestEntry, app::AppInfo)
    path = entry_path(entry)
    return path === nothing ?
        write_shim(d, app, "environments/apps/$(entry.name)"; relative_load_path = true) :
        write_shim(d, app, path; relative_load_path = false)
end

# Bring already-installed shims up to the current `SHIM_VERSION` without a
# re-add: rewrite every existing shim whose embedded version is stale, purely
# from the recorded manifest. Called at the start of the mutating operations.
function migrate_shims!(d::DepotStack; io::IO = stderr_f())
    isfile(app_manifest_file(d)) || return nothing
    migrated = 0
    for (uuid, entry) in read_app_manifest(d)
        for app in values(entry.apps)
            shim = shim_path(d, app.name)
            isfile(shim) || continue                       # only upgrade existing shims
            installed_shim_version(shim) == SHIM_VERSION && continue
            rewrite_shim(d, entry, app)
            migrated += 1
        end
    end
    migrated == 0 ||
        printpkgstyle(io, :Updated, "$migrated app shim$(migrated == 1 ? "" : "s") to format v$SHIM_VERSION", color = Base.info_color())
    return nothing
end

